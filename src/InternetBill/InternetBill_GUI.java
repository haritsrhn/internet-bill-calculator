    public static void writeFile()throws IOException{
        
        FileWriter write = new FileWriter("BillInfo.txt");
        PrintWriter text = new PrintWriter(write);
        
        text.printf("%-13s%-15s%-17s%-17s%-20s%-17s%n","Cust. ID", "Cust. Name", "Phone Number", "Subscription", "Period of Time", "Total Amount");
        for(InternetBill b: netBill)
            text.write(b.toStringForFile());
        write.close();
    } 
    
    public static void readFile()throws IOException{
        
        File inFile = new File("BillInfo.txt");
        Scanner scan = new Scanner(inFile);
        
        while(scan.hasNextLine()){
            
            String line = scan.nextLine();
            outputTA.append(line+"\n");
            
        }
        scan.close();
    }

