    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try{
            for(InternetBill b: netBill){
            int id = Integer.parseInt(idTF.getText());
            String name = nameTF.getText();
            String phone = phoneTF.getText();
            String period = (String)periodCB.getSelectedItem();
            String speed = (String)sCB.getSelectedItem();
            InternetBill bill = new InternetBill(id, name, phone, speed, period);
            netBill.set(index, bill);
            bill.calculateBill();
            bill.getTotalAmount();
            writeFile();
            }
            JOptionPane.showMessageDialog(null, "Data has been updated successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
            }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        try{
        boolean found = false;
        String delete = JOptionPane.showInputDialog(null,"Delete Customer ID Number:");
        for(InternetBill b: netBill){
            if(String.valueOf(b.getId()).equalsIgnoreCase(delete)){
                int dialogResult = JOptionPane.showConfirmDialog(null, "Delete this data", "Delete", JOptionPane.YES_NO_OPTION);
                if(dialogResult == JOptionPane.YES_OPTION){
                    index = netBill.indexOf(b);
                    JOptionPane.showMessageDialog(null, "Data has been deleted", "Information", JOptionPane.INFORMATION_MESSAGE);
                    netBill.remove(index);
                    found=true;
                    writeFile();
                    break;
                }
            }
        }
        if(found==false){
            JOptionPane.showMessageDialog(null, "Data is not found. Try again", "Error", JOptionPane.ERROR_MESSAGE);}
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try{
            boolean found=false;
        String search = JOptionPane.showInputDialog(null,"Search Customer ID Number:");
        for(InternetBill b: netBill){
            if(String.valueOf(b.getId()).equalsIgnoreCase(search)){
                index = netBill.indexOf(b);
                idTF.setText(String.valueOf(b.getId()));
                nameTF.setText(b.getName());
                phoneTF.setText(b.getPhone());
                sCB.setSelectedItem(b.getSpeed());
                periodCB.setSelectedItem(b.getPeriod());
                outputTA.setText(b.toString());
                found=true;
                JOptionPane.showMessageDialog(null, "Data is found", "Information", JOptionPane.INFORMATION_MESSAGE);
                break; 
                }
            }
        if(found==false){
        JOptionPane.showMessageDialog(null, "Data is not found. Try again", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

